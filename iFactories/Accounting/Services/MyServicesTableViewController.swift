        // Configure the cell...

        if let ser = myServices[indexPath.row] as? Service
        {
            if ser.id > 90
            {
                cell.titleLB.text = ser.description
                
            }else{
                let ind = ser.id - 1
                if let cs = MainInfo.services[ind] as? Service
                {
                cell.titleLB.text = cs.name
                }
            }
            cell.dateLB.text = ser.date
        }
        return cell
